# Enteros
x = 10 
print(x)
print(type(x)) # int

# string
y = "hola mundo"
print(y)
print(type(y)) # str

# flotantes
z = 45.53
print(z)
print(type(z)) # float

# booleanos
w = True # tambien puede ser false, solo que con la F mayuscula
print(w)
print(type(w)) # bool